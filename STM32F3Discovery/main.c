void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
	if (huart == &huart2) { // UART2
		RxCompleted = true;

		if (TxCompleted == true) {
			TxCompleted = false;
			HAL_UART_Transmit_IT(&huart2, str, 1);
		}
	}
	if (huart == &huart3) { // UART3
		if (UART3_rxBuffer[0] == '0') {
			HAL_UART_AbortTransmit(&huart3);

			HAL_UART_Transmit(&huart3, (uint8_t*) "\n\r", 2, 100);
			HAL_UART_Transmit(&huart3, UART3_rxBuffer, 1, 100);
			HAL_UART_Transmit(&huart3, (uint8_t*) "\n\rStop\n\r",
					sizeof("\n\rStop\n\r"), 100);
		}

		if (UART3_rxBuffer[0] == '1') {
			HAL_UART_Transmit(&huart3, (uint8_t*) "\n\r", 2, 100);
			HAL_UART_Transmit(&huart3, UART3_rxBuffer, 1, 100);
			HAL_UART_Transmit(&huart3, (uint8_t*) "\n\rStart\n\r",
					sizeof("\n\rStart\n\r"), 100);

			HAL_UART_Transmit_DMA(&huart3, dma_buffer, 2000);
		}

		if (UART3_rxBuffer[0] != '0' && UART3_rxBuffer[0] != '1') {
			HAL_UART_Transmit(&huart3, (uint8_t*) "\n\rPress 0 or 1\n\r",
					sizeof("\n\rPress 0 or 1\n\r"), 100);
		}

		HAL_UART_Receive_DMA(&huart3, UART3_rxBuffer, 1);
	}
}

void HAL_UART_TxCpltCallback(UART_HandleTypeDef *huart) {
	if (huart == &huart2) { // UART2
		TxCompleted = true;

		if (RxCompleted == true) {
			RxCompleted = false;
			HAL_UART_Receive_IT(&huart2, str, 1);
		}

	}
	if (huart == &huart3) { // UART3
		uint16_t i;
		toggle = !toggle;

		for (i = 1000; i < 1998; i++) {
			if (toggle)
				dma_buffer[i] = '&';
			else
				dma_buffer[i] = 'z';
		}

		dma_buffer[1998] = '\r';
		dma_buffer[1999] = '\n';

		//HAL_UART_Transmit_DMA(&huart1, dma_buffer, 2000);
		HAL_GPIO_TogglePin(LD4_GPIO_Port, LD4_Pin);
	}
}

